package introMethod;

public class MCOC {
       public static void multiplication() {
    	   int a=10;
    	   int b=5;
    	   System.out.println("The value is ="+(a*b));
       }
       public void div() {
    	   int a=20;
    	   int b=5;
    	   System.out.println("The value is ="+(a/b));
       }
       public static void addition() {
    	   int c=10;
    	   int d=20;
    	   System.out.println("The value is ="+(c+d));
       }
       public void minus() {
    	   int a=40;
    	   int b=20;
    	   System.out.println("The value is ="+(a-b));
       }
}
